instrInfiniteUsage i =
  let (use,def) = SpecsGen.operandInfo i
      usages = [infRegClassUsage (InfiniteRegisterClass rc) | TemporaryInfo {oiRegClass = InfiniteRegisterClass rc} <- use++def]
  in maximum (usages ++ [0])

